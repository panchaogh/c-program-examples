#include<stdio.h>

/**
 * 编写程序,将用户输入的华氏温度转换成摄氏温度,并输出结果。
 * 摄氏温度与华氏温度之间的转换公式为 c = 5 / 9 × ( F − 32 )
 * @return
 */
int main(void) {
    float f, c;
    printf("请输入华氏温度:");
    scanf("%f", &f); /*用户根据提示输入华氏温度*/
    c = 5.0 / 9 * (f - 32); /*根据公式计算对应的摄氏温度*/
    printf("转换后的摄氏温度为:%f\n", c); /*输出摄氏温度*/
    return 0;
}